public class Mahasiswa {
  String firstName;
  String lastName;
  int nim;
  String universitas;
  String jurusan;
  
   Mahasiswa(String firstName, String lastName, int nim, String universitas, String jurusan) {
    this.firstName = firstName;
    this.lastName = firstName;
    this.nim = nim;
    this.universitas = universitas;
    this.jurusan = jurusan;
  }
}
